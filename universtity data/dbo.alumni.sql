CREATE TABLE [dbo].[Alumni]
(
	[Alumni ID] INT NOT NULL PRIMARY KEY, 
    [Name] NVARCHAR(50) NOT NULL, 
    [Address] NVARCHAR(50) NOT NULL, 
    [Contact information] NVARCHAR(50) NOT NULL, 
    [Graduation year] NVARCHAR(50) NOT NULL, 
    [Degree obtained] NVARCHAR(50) NOT NULL, 
    [Employment details] NVARCHAR(50) NOT NULL
)
